const mongoose = require('mongoose');

const ClothesSchema = new mongoose.Schema({
    image: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    stock: {
        type: Number,
        required: true
    }
})

const Clothes = mongoose.model('Clothes', ClothesSchema);

module.exports = Clothes;